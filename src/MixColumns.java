    public void setDecrypt(boolean decrypt) {
        this.decrypt = decrypt;
    }

    private boolean decrypt;

    private static final byte[][] matrix = {
            { 0x03, 0x0b },
            { 0x01, 0x0d },
            { 0x01, 0x09 },
            { 0x02, 0x0e }
            // 左加密右解密
    };

    private final byte a, b, c, d;

    /**
     * 根據是否解密決定固定多項式的係數
     * @param decrypt 是否解密
     */
    public MixColumns(boolean decrypt) {
        setDecrypt(decrypt);
        int arrayIndex = decrypt ? 1 : 0;
        a = matrix[0][arrayIndex];  //  x^3 的係數
        b = matrix[1][arrayIndex];  //  x^2 的係數
        c = matrix[2][arrayIndex];  //  x^1 的係數
        d = matrix[3][arrayIndex];  //  x^0 的係數
    }

    // Multiplies two bytes in galois field 2^8
    private byte multiply(byte a, byte b) {
        byte returnValue = 0;
        byte temp;
        while (a != 0) {
            if ((a & 1) != 0)
                returnValue = (byte) (returnValue ^ b);
            temp = (byte) (b & 0x80);
            b = (byte) (b << 1);
            if (temp != 0)
                b = (byte) (b ^ 0x1b);
            a = (byte) ((a & 0xff) >> 1);
        }
        return returnValue;
    }

    /**
     * 混行運算
     * @param state 狀態矩陣
     * @return 傳回轉換過的狀態矩陣
     */
    public byte[][] mixColumns(byte[][] state, boolean lookupTableQuickly) {
        int[] temp = new int[4];
        if (!lookupTableQuickly) {
            for (int i = 0; i < 4; i++) {
                temp[0] = multiply(d, state[0][i]) ^ multiply(a, state[1][i])
                        ^ multiply(b, state[2][i]) ^ multiply(c, state[3][i]);
                temp[1] = multiply(c, state[0][i]) ^ multiply(d, state[1][i])
                        ^ multiply(a, state[2][i]) ^ multiply(b, state[3][i]);
                temp[2] = multiply(b, state[0][i]) ^ multiply(c, state[1][i])
                        ^ multiply(d, state[2][i]) ^ multiply(a, state[3][i]);
                temp[3] = multiply(a, state[0][i]) ^ multiply(b, state[1][i])
                        ^ multiply(c, state[2][i]) ^ multiply(d, state[3][i]);
                for (int j = 0; j < 4; j++)
                    state[j][i] = (byte) (temp[j]);
            }
        } else {
            if (!isDecrypt()) {
                for (int i = 0; i < 4; i++) {
                    temp[0] = MULTIPLICATION_02(state[0][i]) ^ MULTIPLICATION_03(state[1][i])
                            ^ multiply(b, state[2][i]) ^ multiply(c, state[3][i]);
                    temp[1] = multiply(c, state[0][i]) ^ MULTIPLICATION_02(state[1][i])
                            ^ MULTIPLICATION_03(state[2][i]) ^ multiply(b, state[3][i]);
                    temp[2] = multiply(b, state[0][i]) ^ multiply(c, state[1][i])
                            ^ MULTIPLICATION_02(state[2][i]) ^ MULTIPLICATION_03(state[3][i]);
                    temp[3] = MULTIPLICATION_03(state[0][i]) ^ multiply(b, state[1][i])
                            ^ multiply(c, state[2][i]) ^ MULTIPLICATION_02(state[3][i]);
                    for (int j = 0; j < 4; j++)
                        state[j][i] = (byte) (temp[j]);
                }
            } else {
                for (int i = 0; i < 4; i++) {
                    temp[0] = MULTIPLICATION_0E(state[0][i]) ^ MULTIPLICATION_0B(state[1][i])
                            ^ MULTIPLICATION_0D(state[2][i]) ^ MULTIPLICATION_09(state[3][i]);
                    temp[1] = MULTIPLICATION_09(state[0][i]) ^ MULTIPLICATION_0E(state[1][i])
                            ^ MULTIPLICATION_0B(state[2][i]) ^ MULTIPLICATION_0D(state[3][i]);
                    temp[2] = MULTIPLICATION_0D(state[0][i]) ^ MULTIPLICATION_09(state[1][i])
                            ^ MULTIPLICATION_0E(state[2][i]) ^ MULTIPLICATION_0B(state[3][i]);
                    temp[3] = MULTIPLICATION_0B(state[0][i]) ^ MULTIPLICATION_0D(state[1][i])
                            ^ MULTIPLICATION_09(state[2][i]) ^ MULTIPLICATION_0E(state[3][i]);
                    for (int j = 0; j < 4; j++)
                        state[j][i] = (byte) (temp[j]);
                }
            }
        }
        return state;
    }

